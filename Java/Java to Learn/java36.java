//do while loop
//syntax:
/*
 do{
    //do something
 }while(condition)
 
 */
//so it will definitely ao something one time
//then it'll do something based on the condition

public class java36 {
    public static void main(String[] main){
        int counter=1;
        do{
            System.out.println("hello world");
            counter++;
        }while(counter<=10);
    }
}
