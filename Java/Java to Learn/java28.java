//while loop 

//syntax:
/*
 while(condition){
    //do something
    loop termination condition
 }
 */
//print hello world 100 times
public class java28 {
    public static void main(String[] args){
        int counter = 0;
        while(counter<100){
            System.out.println("hello world");
            counter++;
        }

    }
}
//if the condition becomes always true then it is infinite loop
