//type casting
import java.util.*;
public class java12 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        float a = 25.99999f;
        int b=(int)a;
        System.out.println(b);
        //explicit conversion(shortening)

        char ch='a';
        int number=ch;
        System.out.println(number);
    }
}
